// The area of a circumference
// Pi 8 radius to the 2nd potence, or radius times radius.

let radius = 10;
const PI = 3.141592;
let area = PI * (radius * radius);

console.log("Circumference Radius: " + radius + " m");
console.log("We know that 'pi' has a constant value of: " + PI);
console.log("Therefore the area of this circumference is: " + area + " m2.");

console.log("...");
console.log("More precisely, 'pi' with 15 algaritms: " + Math.PI);
