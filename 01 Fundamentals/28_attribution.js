// '=' // Assignment operator in JavaScript
// The assignment operator is used to assign a value to a variable.
const a = 7
let b = 3

// The assignment operator can also be used to assign the value of one variable to another.
b += a // b = b + a // Prints: 10 (3 + 7)
console.log(b)

// The assignment operator can be combined with arithmetic operators to perform operations and assign the result to a variable.
b -= 4 // b = b - 4 // Prints: 6 (10 - 4)
console.log(b)

// The assignment operator can also be used with multiplication and modulus operators.
b *= 2 // b = b * 2 // Prints: 12 (6 * 2)
console.log(b)

// The assignment operator can also be used with division and modulus operators.
b /= 2 // b = b / 2 // Prints: 6 (12 / 2)
console.log(b)

// The assignment operator can also be used with the modulus operator. This shows the 'rest of the division' operation.
b %= 2 // b = b % 2 // Prints: 0 (6 % 2)
console.log(b)